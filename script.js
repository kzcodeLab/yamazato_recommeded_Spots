  const descJa = document.createElement("p");
  descJa.className = "card__description-line card__description-line--ja";
  descJa.textContent = item.descriptionJa;
  description.appendChild(descJa);

  const descEn = document.createElement("p");
  descEn.className = "card__description-line card__description-line--en";
