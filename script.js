const resetHeights = (elements) => {
  elements.forEach((el) => {
    if (el) {
      el.style.minHeight = "";
      el.style.height = "";
    }
  });
};

const synchronizeHeights = () => {
  const grids = document.querySelectorAll('.card-grid');
  grids.forEach((grid) => {
    const cards = Array.from(grid.querySelectorAll('.card'));
    if (!cards.length) return;

    const descriptions = cards.map((card) =>
      card.querySelector('.card__description')
    );
    resetHeights(descriptions);

    const infoRowsPerCard = cards.map((card) =>
      Array.from(card.querySelectorAll('.card__info-item'))
    );
    infoRowsPerCard.forEach((rows) => resetHeights(rows));

    const maxDescription = Math.max(
      0,
      ...descriptions.map((el) => (el ? el.offsetHeight : 0))
    );
    descriptions.forEach((el) => {
      if (el) el.style.minHeight = maxDescription + 'px';
    });

    const maxRowCount = Math.max(
      0,
      ...infoRowsPerCard.map((rows) => rows.length)
    );
    for (let i = 0; i < maxRowCount; i += 1) {
      const rowsAtIndex = infoRowsPerCard
        .map((rows) => rows[i])
        .filter((row) => Boolean(row));
      if (!rowsAtIndex.length) continue;
      const rowHeight = Math.max(...rowsAtIndex.map((row) => row.offsetHeight));
      rowsAtIndex.forEach((row) => {
        row.style.minHeight = rowHeight + 'px';
      });
    }
  });
};

let resizeTimer;
const handleResize = () => {
  window.clearTimeout(resizeTimer);
  resizeTimer = window.setTimeout(synchronizeHeights, 150);
};

const populateCategories = () => {
  Object.entries(categoryData).forEach(([key, items]) => {
    const grid = document.querySelector(
      '[data-category="' + key + '"] .card-grid'
    );
    if (!grid) return;
    items.forEach((item) => {
      grid.appendChild(createCardElement(item, key));
    });
  });
};

const initSmoothScroll = () => {
  document.querySelectorAll(".hero__link").forEach((link) => {
    link.addEventListener("click", (event) => {
      const hash = link.getAttribute("href");
      if (!hash?.startsWith("#")) return;
      const target = document.querySelector(hash);
      if (!target) return;
      event.preventDefault();
      target.scrollIntoView({ behavior: "smooth", block: "start" });
    });
  });
};


populateCategories();
initSmoothScroll();

synchronizeHeights();
window.addEventListener('load', synchronizeHeights);
window.addEventListener('resize', handleResize);
